    public boolean EmailCheck(String Email){
boolean emailCheck=false;
        if(Email.contains("@")){
            if (Email.contains(".com")) {
                emailCheck = true;
            }
            else {
                JOptionPane.showMessageDialog(this, "Incorrect Email Please Re-Enter Email","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else  {
            JOptionPane.showMessageDialog(this, "Incorrect Email Please Re-Enter Email","Error", JOptionPane.ERROR_MESSAGE);
        }
        return emailCheck;
    }


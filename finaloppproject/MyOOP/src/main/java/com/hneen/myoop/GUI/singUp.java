    private void  usersingup (String name, String address,String email,String username, String password){
        DB t=new DB();
        Connection dbconn=t.connectDB();
        int last_id=0;
        PreparedStatement st1=null;
        PreparedStatement st=null;
        PreparedStatement insert_id=null;
        PreparedStatement id_p=null;
        ResultSet resultSet=null;
        try {
             id_p=(PreparedStatement) dbconn.prepareStatement("SELECT * FROM person");
             resultSet=id_p.executeQuery();

           while (resultSet.next()){
                    last_id = resultSet.getInt("id");
            }

            if (customer_RadioButton1.isSelected()){
                int id=last_id+1;
                 insert_id=(PreparedStatement) dbconn.prepareStatement("insert into person(id) value (?)");
                insert_id.setInt(1,id);
                int res =insert_id.executeUpdate();
                if (res==1){
                     st = (PreparedStatement) dbconn.prepareStatement("insert into customer(coustomer_id,coustomer_name ,coustomer_address ,coustomer_Email) values (?,?,?,?)  ");
                    st.setInt(1, id);
                    st.setString(2, name);
                    st.setString(3, address);
                    st.setString(4, email);
                    int res2 = st.executeUpdate();
                    if (res2 == 1) {
                         st1 = (PreparedStatement) dbconn.prepareStatement("insert into account(username ,password,person_id,rule) values (?,?,?,?)");
                        st1.setString(1, username);
                        st1.setString(2, password);
                        st1.setInt(3, id);
                        st1.setString(4, "c");
                        int res1 = st1.executeUpdate();
                        if (res1==1)
                        JOptionPane.showMessageDialog(this, "customer Added", "done!", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
            else  if (Employee_RadioButton2.isSelected()){
                int id=last_id+1;
                 insert_id=(PreparedStatement) dbconn.prepareStatement("insert into person(id) value (?)");
                insert_id.setInt(1,id);
                int res =insert_id.executeUpdate();
                if (res==1){
                     st = (PreparedStatement) dbconn.prepareStatement("insert into employee(Employee_id,Employee_name ,Employee_address ,Employee_Email,supervise_id) values (?,?,?,?,4)  ");
                    st.setInt(1, id);
                    st.setString(2, name);
                    st.setString(3, address);
                    st.setString(4, email);
                    int res2 = st.executeUpdate();
                    if (res2 == 1) {
                         st1 = (PreparedStatement) dbconn.prepareStatement("insert into account(username ,password,person_id,rule) values (?,?,?,?)");
                        st1.setString(1, username);
                        st1.setString(2, password);
                        st1.setInt(3, id);
                        st1.setString(4, "e");
                        int res1 = st1.executeUpdate();
                        if (res1==1)
                            JOptionPane.showMessageDialog(this, "Employee Added", "done!", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }

        }catch (Exception ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Error in adding","Error", JOptionPane.ERROR_MESSAGE);
        }finally {
            try {
                dbconn.close();
                insert_id.close();
                resultSet.close();
                id_p.close();
                st.close();
                st1.close();

            }catch (SQLException e){
                e.printStackTrace();
            }
        }

    }

